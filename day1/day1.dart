void main() {
  // Contoh penggunaan if-else
  int angka = 7;

  if (angka % 2 == 0) {
    print("$angka adalah bilangan genap");
  } else {
    print("$angka adalah bilangan ganjil");
  }

  // Contoh penggunaan loop (for)
  print("\nPerulangan angka dari 1 sampai 5:");
  for (int i = 1; i <= 5; i++) {
    print("Angka ke-$i");
  }
}
